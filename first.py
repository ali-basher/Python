# ----------------------------------
# -- Escaping --
# --------------
# Escape Sequences Characters
# \b       => Back Space
# \newline => Escape New Line + \
# \        => Escape Back Slash
# \'       => Escape Single Quote
# \"       => Escape Double Quote
# \n       => Line Feed Character
# \r       => Carriage Return
# \t       => Horizontal Tab
# \xhh     => Character Hex Value
# ----------------------------------

# Back Space
print("Hello In Java\b")  # Will Remove Character 'a'

# Escape New Line + \
print("Hello \
python \
for \
Everyone")

# Escape Back Slash
print("I Love Back Slash\\")

# Escape Single Quote
print('I Love Single Quote \'Test\'')

# Escape Double Quote
print("I Love Single Quote \"Test\"")

# Line Feed
print("Hello World\nSecond Line")

# Carriage Return
print('123456\rAbcd')  # OutPut => Abcd56

# Horizontal Tab
print("Hello\t World")

# Character Hex Value
print("\x41\x4C\x49")
